    private void ButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAddActionPerformed
        
        String nom_room = TextName.getText().trim();
        String eff_room_string = TextName1.getText();
        Integer eff_room = Integer.parseInt(eff_room_string);
        
        // Vérifiez si le champ de texte n'est pas vide
            if (!nom_room.isEmpty() && !eff_room_string.isEmpty()) {
                try {
                    // Obtenez une connexion à la base de données
                    Connection connection = Connector.getConnection();

                    // Vérifiez si la classe existe déjà
                    if (!RoomExists(connection, nom_room)) {
                        // Si le  n'existe pas, ajoutez-la à la base de données
                        addRoom(connection, nom_room, eff_room);
                        // Affichez une boîte de dialogue de succès
                        javax.swing.JOptionPane.showMessageDialog(this, "La Salle '" + nom_room + "' a été ajoutée avec succès.");

                        // Fermez la connexion à la base de données
                        Connector.closeConnection(connection);
                    } else {
                        // Si la classe existe déjà, affichez une boîte de dialogue d'erreur
                        javax.swing.JOptionPane.showMessageDialog(this, "Erreur : La Salle '" + nom_room + "' existe déjà.");
                    }
                } catch (SQLException e) {
                    // En cas d'erreur de base de données, affichez un message d'erreur
                    javax.swing.JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout de la Salle : " + e.getMessage());
                }
            } else {
                // Si le champ de texte est vide, affichez un message d'erreur
                javax.swing.JOptionPane.showMessageDialog(this, "The fields Room name and Room size can't be empty");
            }
        
    }//GEN-LAST:event_ButtonAddActionPerformed

